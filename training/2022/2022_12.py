assert (
    INPUT.count("S") == INPUT.count("E") == 1
), "there should be exactly one start square and one ending square"

heights_mapping = dict(zip(ascii_lowercase, range(1, len(ascii_lowercase) + 1)))
# add the starting and ending squares heights
heights_mapping["S"] = 1
heights_mapping["E"] = 26

rows = [
    list(map(heights_mapping.get, row)) for row in INPUT.splitlines() if row.strip()
]

# start an empty graph
G = (
    nx.DiGraph()
)  # I need a directed graph because being allowed to go from a to b does not imply being allowed to go from b to a

# populate it with only the accessible edges i.e. those that are maximum 1 unit above me
for row_ind in range(len(rows)):
    for col_ind in range(len(rows[0])):
        if (row_ind > 0) and rows[row_ind][col_ind] >= rows[row_ind - 1][col_ind] - 1:
            G.add_edge((row_ind, col_ind), (row_ind - 1, col_ind))
        if (row_ind < len(rows) - 1) and rows[row_ind][col_ind] >= rows[row_ind + 1][
            col_ind
        ] - 1:
            G.add_edge((row_ind, col_ind), (row_ind + 1, col_ind))
        if (col_ind > 0) and rows[row_ind][col_ind] >= rows[row_ind][col_ind - 1] - 1:
            G.add_edge((row_ind, col_ind), (row_ind, col_ind - 1))
        if (col_ind < len(rows[0]) - 1) and rows[row_ind][col_ind] >= rows[row_ind][
            col_ind + 1
        ] - 1:
            G.add_edge((row_ind, col_ind), (row_ind, col_ind + 1))

# find start and end coordinates
clean_heightmap = INPUT.replace("\n", "").replace(" ", "").strip()
start = (
    (start_coord := clean_heightmap.index("S")) // len(rows[0]),
    start_coord % len(rows[0]),
)
end = (
    (end_coord := clean_heightmap.index("E")) // len(rows[0]),
    end_coord % len(rows[0]),
)
print(nx.shortest_path_length(G, start, end))

